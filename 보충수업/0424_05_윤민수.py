num = int(input("숫자를 입력하세요. : "))
# 2부터 입력받은 숫자까지 반복하여 소수를 찾음
for i in range(2, num + 1) : 
    # i 변수에 저장된 값이 소수인지 확인하기 위해 2부터 자기자신까지 반복
    for j in range(2, i + 1) : 
        # 나머지가 0이면
        if i % j == 0 : 
            break       # for j 를 빠져 나간다.
     
    # 자기 자신을 나누어 떨어진 것이 맞나?    
    if i == j :     # 두 수가 같은가? i는 소수 판단 할 수. j는 나누는 수
        print(i, end=" ")
